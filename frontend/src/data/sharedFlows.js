export const SHARED_SPACE_STORAGE_KEY = 'xrag-shared-space-v1';

export const loadUserSharedFlows = () => {
  try {
    const raw = localStorage.getItem(SHARED_SPACE_STORAGE_KEY);
    if (!raw) return [];
    const parsed = JSON.parse(raw);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
};

export const saveUserSharedFlow = (flow, meta = {}) => {
  const existing = loadUserSharedFlows();
  const entry = {
    id: `shared-user-${Date.now()}`,
    name: meta.name || flow.name || 'My RAG Flow',
    description: meta.description || '',
    author: meta.author || 'You',
    authorInitials: (meta.author || 'Me').slice(0, 2).toUpperCase(),
    authorColor: 'bg-indigo-600',
    tags: meta.tags || [],
    likes: 0,
    views: 0,
    createdAt: new Date().toISOString().split('T')[0],
    accentFrom: '#6366f1',
    accentTo: '#4f46e5',
    isUserShared: true,
    nodes: flow.nodes || [],
    edges: flow.edges || [],
  };
  const next = [entry, ...existing].slice(0, 20);
  localStorage.setItem(SHARED_SPACE_STORAGE_KEY, JSON.stringify(next));
  return entry;
};
